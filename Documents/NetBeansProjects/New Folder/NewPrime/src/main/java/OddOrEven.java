public class OddOrEven {
    public static void main(String[] args) {
        int AJ;
        Scanner Num = new Scanner(System.in);
        System.out.println("Enter a new number: ");
        AJ = Num.nextInt();
        if(AJ % 2 == 0){
            System.out.println("Number is even");
        } else{
            System.out.println("Number is odd");
           
        }}
            
    
}
